#ifndef MOUSE_CURSOR_POS_CALLBACK_FUNCTIONS_H
#define MOUSE_CURSOR_POS_CALLBACK_FUNCTIONS_H

void cursor_pos_callback(GLFWwindow *window, double xpos, double ypos) {
    if (firstMouse) {
        lastX = xpos;
        lastY = ypos;
        firstMouse = false;
    }

    float xoffset = xpos - lastX;
    float yoffset = lastY - ypos;

    lastX = xpos;
    lastY = ypos;

    camera.ProcessMouseMovement(xoffset, yoffset);
}

#endif