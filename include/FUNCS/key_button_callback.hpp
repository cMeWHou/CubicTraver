#ifndef KEY_BUTTON_CALLBACK_FUNCTIONS_H
#define KEY_BUTTON_CALLBACK_FUNCTIONS_H

void key_button_callback(GLFWwindow *window, int key, int scancode, int action, int mode) {
    if (key == GLFW_KEY_ESCAPE && action == GLFW_PRESS)
        glfwSetWindowShouldClose(window, GL_TRUE);

    if (key >= 0 && key < 256) {
        if (action == GLFW_PRESS)
            keys[key] = true;
        else if (action == GLFW_RELEASE)
            keys[key] = false;
    }
}

#endif